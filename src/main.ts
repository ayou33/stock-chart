const bars: Bar[] = M30.map(d => {
  const timestamp = Number(d.u) * 1000

  return {
    open: parseFloat(d.o),
    high: parseFloat(d.h),
    low: parseFloat(d.l),
    close: parseFloat(d.c),
    date: timestamp,
    volume: Number(d.v),
    DT: new Date(timestamp),
  }
})

const ex = extent(bars, d => d.low, d => d.high)
const tweak = (ex[1] - ex[0]) / 10

chart.attach({
  resolveSymbol (symbol: string): Promise<SymbolDescriber> {
    console.log('resolveSymbol', symbol)
    return Promise.resolve({
      name: 'BTCUSD',
      code: 'BTCUSD',
      description: '比特币',
      exchange: 'EX',
      periodicity: {
        interval: 1,
      },
    })
  },

  read (symbol: SymbolDescriber): Promise<FeedSnapshot> {
    console.log('read', symbol)
    return new Promise(resolve => {
      setTimeout(() => {
        resolve({
          data: bars,
        })
      }, 1000)
    })
  },

  subscribe (symbol: SymbolDescriber, streamCallback: (patch: Patch) => void): Fn {
    function randomInterval () {
      return getRandomInt(200, 1000 * 2)
    }

    function feed () {
      setTimeout(() => {
        streamCallback({
          ...symbol,
          price: getRandomInt(ex[0] + tweak * 4, ex[1] - tweak * 4) + Math.random(),
          time: new Date().getTime(),
        })
        feed()
      }, randomInterval())
    }

    feed()

    return () => {
    }
  },

  unSubscribe (symbol: SymbolDescriber) {
    console.log('unsub', symbol)
  },
})

